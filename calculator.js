const calculator = {
  add: (input1, input2) => {
    return input1 + input2;
  },
  subtract: (input1, input2) => {
    return input1 - input2;
  },
  multiply: (input1, input2) => {
    return input1 * input2;
  },
  divide: (input1, input2) => {
    return input1 / input2;
  },
};

module.exports = calculator;
